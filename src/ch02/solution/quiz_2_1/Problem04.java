package ch02.solution.quiz_2_1;

public class Problem04 {
    /**
     * 두 줄 출력하기
     * 첫줄 -> "Hello, Java"
     * 둘째 줄 -> "Let`s start coding."
     */

    public static void main(String[] args) {
        // println() -> 출력 후 자동으로 줄바꿈
        System.out.println("Hello, Java!");
        System.out.println("Let's start coding");
        // print("....\n") -> 문자열 끝에 개행 문자인 \n을 붙여 줄바꿈
        System.out.print("Hello, Java!\n");
        System.out.print("Let's start coding\n");

    }
}
